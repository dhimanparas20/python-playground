    def export_csv(
        self,
        filepath: str,
        query: Optional[Dict[str, Any]] = None,
        id_column: str = "_id",
    ) -> int:
        """
        Export documents to a CSV file.

        Args:
            filepath: Path to output CSV file.
            query: Optional MongoDB query filter.
            id_column: Column name for document id. Defaults to "_id".

        Returns:
            Number of documents exported.
        """
        data = self.get_all(query=query)
        if not data:
            path = Path(filepath)
            path.parent.mkdir(parents=True, exist_ok=True)
            with open(path, "w", newline="", encoding="utf-8") as f:
                writer = csv.writer(f)
                writer.writerow([id_column])
            return 0

        all_fields: set[str] = set()
        for entry in data.values():
            all_fields.update(entry.keys())
        fieldnames = [id_column] + sorted(all_fields)

        path = Path(filepath)
        path.parent.mkdir(parents=True, exist_ok=True)
        with open(path, "w", newline="", encoding="utf-8") as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames, extrasaction="ignore")
            writer.writeheader()
            for doc_id, doc_data in data.items():
                row = {id_column: doc_id}
                row.update({k: str(v) for k, v in doc_data.items()})
                writer.writerow(row)
        return len(data)

    def import_csv(
        self,
        filepath: str,
        id_column: str = "_id",
        overwrite: bool = False,
    ) -> int:
        """
        Import documents from a CSV file.

        Args:
            filepath: Path to input CSV file.
            id_column: Column name that contains document id.
            overwrite: If True, overwrite existing documents.

        Returns:
            Number of documents imported.

        Raises:
            ValueError: If id_column is not found in CSV headers.
        """
        with open(filepath, "r", newline="", encoding="utf-8") as f:
            reader = csv.DictReader(f)
            if reader.fieldnames is None or id_column not in reader.fieldnames:
                raise ValueError(f"Column '{id_column}' not found in CSV headers: {reader.fieldnames}")
            entries: Dict[str, Dict[str, str]] = {}
            for row in reader:
                doc_id = row.pop(id_column)
                entries[doc_id] = {k: v for k, v in row.items() if v}
        return len(self.bulk_create(entries, overwrite=overwrite))

