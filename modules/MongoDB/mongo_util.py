class MongoUtil:
    """
    Production-ready MongoDB utility class for CRUD operations,
    bulk operations, indexing, TTL, import/export, and secure data hashing.

    Attributes:
        connection_string (str): MongoDB connection URI.
        database (str): Active database name.
        collection (str): Active collection name.
        index_key (str): Key segment used in index names.
        default_ttl (Optional[int]): Default TTL in seconds for documents.
    """

    def __init__(
        self,
        database: str,
        collection: str,
        connection_string: str = "mongodb://localhost:27017",
        index_key: str = "IDX",
        default_ttl: Optional[int] = None,
    ) -> None:
        """
        Initialize MongoUtil instance.

        Args:
            database: Database name (required).
            collection: Collection name (required).
            connection_string: MongoDB connection URI.
                              Defaults to localhost:27017.
            index_key: Key segment used in index names. Defaults to "IDX".
            default_ttl: Default TTL in seconds for documents.
                         If set, adds _createdAt field and a TTL index.
                         Pass None to disable (documents live forever).

        Example:
            >>> util = MongoUtil(
            ...     database="myapp",
            ...     collection="users",
            ...     connection_string="mongodb://localhost:27017",
            ...     default_ttl=3600,
            ... )
        """
        self.connection_string: str = connection_string
        self.database: str = database
        self.collection: str = collection
        self.index_key: str = index_key.upper()
        self.default_ttl: Optional[int] = default_ttl
        self._client: MongoClient = MongoClient(self.connection_string)
        self._db: Database = self._client[self.database]
        self._col: Collection = self._db[self.collection]
        self._ttl_index_created: bool = False

    def _ensure_ttl_index(self) -> None:
        """Create TTL index on _createdAt if default_ttl is set and not yet created."""
        if self.default_ttl is not None and not self._ttl_index_created:
            existing_indexes = self._col.index_information()
            ttl_index_name = f"{self.index_key}_createdAt_ttl"
            if ttl_index_name not in existing_indexes:
                self._col.create_index(
                    "_createdAt",
                    name=ttl_index_name,
                    expireAfterSeconds=self.default_ttl,
                )
            self._ttl_index_created = True

    def _add_timestamp(self, data: Dict[str, Any]) -> Dict[str, Any]:
        """Add _createdAt timestamp if default_ttl is set."""
        if self.default_ttl is not None:
            self._ensure_ttl_index()
            data = {**data, "_createdAt": datetime.now(timezone.utc)}
        return data

    # ──────────────────────────────────────────────
    # CRUD OPERATIONS
    # ──────────────────────────────────────────────

    def create(
        self,
        data: Dict[str, Any],
        id: Optional[str] = None,
        overwrite: bool = False,
    ) -> str:
        """
        Create a new document or overwrite if overwrite is True.
        If id is not provided, MongoDB auto-generates an ObjectId.

        Args:
            data: Dictionary of fields to store.
            id: Unique identifier for the document. Auto-generated if None.
            overwrite: If True, replace existing document entirely.

        Returns:
            The document id (provided or auto-generated by MongoDB).

        Raises:
            ValueError: If document exists and overwrite is False.
        """
        data = self._add_timestamp(data)
        if id is not None:
            if not overwrite and self._col.find_one({"_id": id}):
                raise ValueError(f"Document '{id}' already exists. Use overwrite=True to update.")
            if overwrite:
                self._col.replace_one({"_id": id}, {**data, "_id": id}, upsert=True)
            else:
                self._col.insert_one({**data, "_id": id})
            return id
        else:
            auto_id = str(ObjectId())
            data["_id"] = auto_id
            self._col.insert_one(data)
            return auto_id

    def read(
        self,
        id: str,
        projection: Optional[Dict[str, int]] = None,
    ) -> Optional[Dict[str, Any]]:
        """
        Read a document by id.

        Args:
            id: Unique identifier for the document.
            projection: Optional field projection.
                       e.g., {"username": 1, "email": 1} to include only these.
                       e.g., {"password": 0} to exclude a field.

        Returns:
            Document dict if found, None otherwise.
        """
        doc = self._col.find_one({"_id": id}, projection=projection)
        if doc and "_id" in doc:
            doc["id"] = doc.pop("_id")
        return doc

    def update(self, id: str, data: Dict[str, Any]) -> bool:
        """
        Update specific fields in an existing document.

        Args:
            id: Unique identifier for the document.
            data: Dictionary of fields to update.

        Returns:
            True if document existed and was updated, False otherwise.
        """
        result = self._col.update_one({"_id": id}, {"$set": data})
        return result.matched_count > 0

    def delete(self, id: str) -> bool:
        """
        Delete a document by id.

        Args:
            id: Unique identifier for the document.

        Returns:
            True if document existed and was deleted, False otherwise.
        """
        result = self._col.delete_one({"_id": id})
        return result.deleted_count > 0

    def delete_fields(self, id: str, *fields: str) -> int:
        """
        Delete specific fields from a document.

        Args:
            id: Unique identifier for the document.
            *fields: Field names to remove.

        Returns:
            Number of documents modified (0 or 1).
        """
        if not fields:
            return 0
        unset_dict = {field: "" for field in fields}
        result = self._col.update_one({"_id": id}, {"$unset": unset_dict})
        return result.modified_count

    def exists(self, id: str) -> bool:
        """Check if a document exists."""
        return self._col.count_documents({"_id": id}, limit=1) > 0

    def field_exists(self, id: str, field: str) -> bool:
        """Check if a specific field exists in a document."""
        doc = self._col.find_one({"_id": id}, {field: 1})
        return doc is not None and field in doc

    def keys(self, id: str) -> List[str]:
        """
        Get all field names in a document.

        Args:
            id: Unique identifier for the document.

        Returns:
            List of field names (excluding _id).
        """
        doc = self._col.find_one({"_id": id})
        if not doc:
            return []
        return [k for k in doc.keys() if k != "_id"]

    def values(self, id: str) -> List[Any]:
        """
        Get all values in a document.

        Args:
            id: Unique identifier for the document.

        Returns:
            List of field values.
        """
        doc = self._col.find_one({"_id": id})
        if not doc:
            return []
        return [v for k, v in doc.items() if k != "_id"]

    def length(self, id: str) -> int:
        """
        Get number of fields in a document (excluding _id).

        Args:
            id: Unique identifier for the document.

        Returns:
            Number of fields.
        """
        doc = self._col.find_one({"_id": id})
        if not doc:
            return 0
        return len([k for k in doc.keys() if k != "_id"])

    def increment(self, id: str, field: str, amount: int = 1) -> int:
        """
        Increment a numeric field value.

        Args:
            id: Unique identifier for the document.
            field: Field name to increment.
            amount: Increment amount (can be negative).

        Returns:
            New value after increment, or -1 if document not found.
        """
        result = self._col.find_one_and_update(
            {"_id": id},
            {"$inc": {field: amount}},
            return_document=True,
        )
        if result and field in result:
            return result[field]
        return -1

    def increment_float(self, id: str, field: str, amount: float = 1.0) -> float:
        """
        Increment a float field value.

        Args:
            id: Unique identifier for the document.
            field: Field name to increment.
            amount: Increment amount (can be negative).

        Returns:
            New value after increment, or -1.0 if document not found.
        """
        result = self._col.find_one_and_update(
            {"_id": id},
            {"$inc": {field: amount}},
            return_document=True,
        )
        if result and field in result:
            return float(result[field])
        return -1.0

    # ──────────────────────────────────────────────
    # SET IF NOT EXISTS / GET OR CREATE
    # ──────────────────────────────────────────────

    def set_if_not_exists(self, id: str, field: str, value: Any) -> bool:
        """
        Atomically set a field only if it does not already exist in the document.
        Creates the document with the field if document does not exist.

        Args:
            id: Unique identifier for the document.
            field: Field name to set.
            value: Value to set.

        Returns:
            True if field was set (did not exist), False if already present.
        """
        result = self._col.update_one(
            {"_id": id, field: {"$exists": False}},
            {"$set": {field: value}},
            upsert=True,
        )
        return result.modified_count > 0 or result.upserted_id is not None

    def get_or_create(
        self,
        id: str,
        data: Dict[str, Any],
    ) -> Dict[str, Any]:
        """
        Return existing document data, or create it and return the new data.

        Args:
            id: Unique identifier for the document.
            data: Dictionary to store if document does not exist.

        Returns:
            The document data (existing or newly created).
        """
        doc = self._col.find_one({"_id": id})
        if doc:
            result = {k: v for k, v in doc.items() if k != "_id"}
            result["id"] = id
            return result
        data = self._add_timestamp(data)
        self._col.insert_one({**data, "_id": id})
        data["id"] = id
        return data

    # ──────────────────────────────────────────────
    # TTL OPERATIONS
    # ──────────────────────────────────────────────

    def _resolve_ttl(self, seconds: int) -> int:
        """
        Resolve the effective TTL index seconds to use for _createdAt adjustment.
        When default_ttl is set, uses it. Otherwise looks up or creates a TTL index.
        """
        if self.default_ttl is not None:
            return self.default_ttl
        ttl_index_name = f"{self.index_key}_createdAt_ttl"
        existing = self._col.index_information()
        for name, info in existing.items():
            if info.get("key") == [("_createdAt", 1)] and "expireAfterSeconds" in info:
                return info["expireAfterSeconds"]
        self._col.create_index(
            "_createdAt",
            name=ttl_index_name,
            expireAfterSeconds=seconds,
        )
        return seconds

    def expire(self, id: str, seconds: int) -> bool:
        """
        Set/reset TTL on a document by updating its _createdAt.

        Args:
            id: Unique identifier for the document.
            seconds: Desired remaining TTL in seconds.

        Returns:
            True if updated, False if document not found.
        """
        index_ttl = self._resolve_ttl(seconds)
        delta = index_ttl - seconds
        new_time = datetime.now(timezone.utc) - timedelta(seconds=delta)
        result = self._col.update_one(
            {"_id": id},
            {"$set": {"_createdAt": new_time}},
        )
        return result.matched_count > 0

    def bulk_expire(self, ids: List[str], seconds: int) -> int:
        """
        Set/reset TTL on multiple documents.

        Args:
            ids: List of document identifiers.
            seconds: Desired remaining TTL in seconds.

        Returns:
            Number of documents updated.
        """
        if not ids:
            return 0
        index_ttl = self._resolve_ttl(seconds)
        delta = index_ttl - seconds
        new_time = datetime.now(timezone.utc) - timedelta(seconds=delta)
        result = self._col.update_many(
            {"_id": {"$in": ids}},
            {"$set": {"_createdAt": new_time}},
        )
        return result.modified_count

    def persist(self, id: str) -> bool:
        """
        Remove TTL from a document by removing _createdAt field.

        Args:
            id: Unique identifier for the document.

        Returns:
            True if TTL was removed, False otherwise.
        """
        result = self._col.update_one(
            {"_id": id},
            {"$unset": {"_createdAt": ""}},
        )
        return result.modified_count > 0

    # ──────────────────────────────────────────────
    # COPY / RENAME OPERATIONS
    # ──────────────────────────────────────────────

    def copy(
        self,
        source_id: str,
        dest_id: Optional[str] = None,
        overwrite: bool = False,
    ) -> str:
        """
        Copy a document to a new id.
        If dest_id is not provided, MongoDB auto-generates an ObjectId.

        Args:
            source_id: Source document identifier.
            dest_id: Destination document identifier. Auto-generated if None.
            overwrite: If True, overwrite existing destination.

        Returns:
            The destination id (provided or auto-generated).

        Raises:
            ValueError: If destination exists and overwrite is False.
        """
        data = self.read(source_id)
        if data is None:
            return ""
        clean = {k: v for k, v in data.items() if k != "id"}
        return self.create(clean, id=dest_id, overwrite=overwrite)

    def bulk_copy(
        self,
        copies: Dict[str, str],
        overwrite: bool = False,
    ) -> Dict[str, str]:
        """
        Copy multiple documents using bulk operations.

        Args:
            copies: Dict mapping source_id -> dest_id.
            overwrite: If True, overwrite existing destinations.

        Returns:
            Dict mapping destination ids to themselves.
        """
        if not copies:
            return {}
        source_ids = list(copies.keys())
        bulk_data = self.bulk_read(source_ids)
        new_entries: Dict[str, Dict[str, Any]] = {}
        for src_id, dest_id in copies.items():
            data = bulk_data.get(src_id)
            if data is not None:
                new_entries[dest_id] = {k: v for k, v in data.items() if k != "id"}
        if not new_entries:
            return {}
        return self.bulk_create(new_entries, overwrite=overwrite)

    def rename(self, old_id: str, new_id: str, overwrite: bool = False) -> bool:
        """
        Rename a document by copying to new id and deleting old.

        Args:
            old_id: Current document identifier.
            new_id: New document identifier.
            overwrite: If True, overwrite if new_id already exists.

        Returns:
            True if rename succeeded.

        Raises:
            ValueError: If new_id exists and overwrite is False.
        """
        if not self.exists(old_id):
            return False
        if not overwrite and self.exists(new_id):
            raise ValueError(f"Document '{new_id}' already exists. Use overwrite=True.")
        data = self.read(old_id)
        if data is None:
            return False
        clean_data = {k: v for k, v in data.items() if k != "id"}
        if overwrite:
            self._col.replace_one({"_id": new_id}, {**clean_data, "_id": new_id}, upsert=True)
        else:
            self._col.insert_one({**clean_data, "_id": new_id})
        self._col.delete_one({"_id": old_id})
        return True

